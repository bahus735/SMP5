void DrawCircle(uint8_t *Buffer, uint8_t x, uint8_t y, uint8_t radius, uint8_t color){
    // Bresenham algorithm
    int16_t x_pos = -radius;
    int16_t y_pos = 0;
    int16_t err = 2 - 2 * radius;
	  int16_t e2 = err;
    if (x >= width || y >= height){
        return;
    }
    while ( 1 ){
        SetPixel_EP(Buffer, x - x_pos, y + y_pos, color);
        SetPixel_EP(Buffer, x + x_pos, y + y_pos, color);
        SetPixel_EP(Buffer, x + x_pos, y - y_pos, color);
        SetPixel_EP(Buffer, x - x_pos, y - y_pos, color);
        if (e2 <= y_pos){
            y_pos += 1;
            err += y_pos * 2 + 1;
            if(-x_pos == y_pos && e2 <= x_pos){
                e2 = 0;
            }
        }
        if (e2 > x_pos){
            x_pos += 1;
            err += x_pos * 2 + 1;
        }
        if (x_pos > 0){
            break;
        }
    }
}
 
